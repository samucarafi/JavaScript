let n=10

//n++ adiciona 1 ao n (Pós incremento) usa o valor depois incrementa, voce der um console.log(n++) imprimira ainda o 10
// ++n adiciona 1 ao n antes de seu uso (pre incremento) imprimirá 11
// o mesmo é válido para --

console.log(n++) // 10

console.log(n) // 11

n=10

console.log(++n) // 11